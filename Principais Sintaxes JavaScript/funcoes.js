//Função simples

// Função saudacao: Esta função não recebe parâmetros e simplesmente exibe a mensagem "Olá, Mundo!" no console.
function saudacao() {
    console.log("Olá, Mundo!");  // A função usa o console.log() para exibir a mensagem no console.
}

// Função soma: Esta função recebe dois parâmetros, `a` e `b`, e retorna a soma de ambos.
function soma(a, b) {
    return a + b;  // O comando return devolve a soma de `a` e `b` quando a função é chamada.
}

//Função com parâmetros
function soma(a, b) {
    return a + b;
}




