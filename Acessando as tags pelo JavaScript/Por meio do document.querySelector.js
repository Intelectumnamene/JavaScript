
//1. Por meio do document.querySelector

const elemento = document.querySelector('tag'); // Substitua 'tag' pelo nome da tag

//Para acessar a primeira tag <p>:
const paragrafo = document.querySelector('p');
console.log(paragrafo.textContent); // Mostra o conteúdo do parágrafo

//Para acessar uma tag com uma classe específica, como <div class="minha-classe">:
const div = document.querySelector('.minha-classe'); // Seleciona pela classe

//Para acessar uma tag com um ID específico, como <h1 id="meu-id">:
const titulo = document.querySelector('#meu-id'); // Seleciona pelo ID




